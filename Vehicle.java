
public class Vehicle {
	public double weight;
	public double height;
	public double width;
	
	Vehicle(double weight, double height, double width){
		this.weight = weight;
		this.height = height;
		this.width = width;
	}
	public double getHeight() {
		return height;
	}
	public double getWidth() {
		return width;
	}
	public double getWeight() {
		return weight;
	}

}
